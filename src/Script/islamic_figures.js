    const elements = {
        searchInput: document.getElementById('search-input'),
        searchBtn: document.getElementById('search-btn'),
        storiesGrid: document.getElementById('stories-grid'),
        tabBtns: document.querySelectorAll('.tab-btn')
    };

    // متغيرات الحالة
    let currentTab = 'prophets';
    let currentStories = stories.prophets;

    // تهيئة الصفحة
    init();

    function init() {
        setupEventListeners();
        addSearchEffects();
        displayStories(currentStories);
    }

    function setupEventListeners() {
        // أحداث أزرار التبويب
        elements.tabBtns.forEach(btn => {
            btn.addEventListener('click', function() {
                elements.tabBtns.forEach(b => b.classList.remove('active'));
                this.classList.add('active');
                currentTab = this.dataset.tab;
                currentStories = stories[currentTab];
                displayStories(currentStories);
            });
        });

        // أحداث البحث
        elements.searchBtn.addEventListener('click', searchByLesson);
        elements.searchInput.addEventListener('keyup', function(e) {
            if (e.key === 'Enter') searchByLesson();
        });
    }

    function addSearchEffects() {
        const searchInput = elements.searchInput;
        const searchBtn = elements.searchBtn;
        
        searchInput.addEventListener('focus', function() {
            this.parentElement.style.transform = 'scale(1.02)';
            this.parentElement.style.boxShadow = '0 10px 30px rgba(31, 125, 83, 0.3)';
        });
        
        searchInput.addEventListener('blur', function() {
            this.parentElement.style.transform = 'scale(1)';
            this.parentElement.style.boxShadow = '0 5px 20px rgba(0, 0, 0, 0.1)';
        });
        
        searchInput.addEventListener('input', function() {
            if (this.value.length > 0) {
                searchBtn.style.background = 'linear-gradient(135deg, #0e4a30 0%, #165f3f 100%)';
            } else {
                searchBtn.style.background = 'linear-gradient(135deg, #1f7d53 0%, #165f3f 100%)';
            }
        });
    }

    function displayStories(storiesToShow) {
        elements.storiesGrid.innerHTML = '';
        
        if (storiesToShow.length === 0) {
            elements.storiesGrid.innerHTML = '<div class="no-results">لا توجد قصص متطابقة مع بحثك</div>';
            return;
        }
        
        storiesToShow.forEach(story => {
            const storyCard = document.createElement('div');
            storyCard.className = 'story-card';
            
            storyCard.innerHTML = `
                <h3>${story.title}</h3>
                <div class="story-summary">${story.summary}</div>
                <div class="story-content">
                    ${story.content}
                    <div class="story-references">
                        <strong>المصادر والمراجع:</strong>
                        <ul>
                            ${story.references.map(ref => 
                                `<li><span class="ref-source">${ref.source}:</span> ${ref.ref}</li>`
                            ).join('')}
                        </ul>
                    </div>
                </div>
                <div class="story-lessons">
                    <strong>الدلالات:</strong>
                    ${story.lessons.map(lesson => `<span class="lesson-tag">${lesson}</span>`).join('')}
                </div>
                <div class="story-category">${story.category}</div>
            `;
            
            const summary = storyCard.querySelector('.story-summary');
            const content = storyCard.querySelector('.story-content');
            
            storyCard.addEventListener('mouseenter', function() {
                summary.style.display = 'none';
                content.style.display = 'block';
                this.style.height = 'auto';
                this.style.zIndex = '10';
            });
            
            storyCard.addEventListener('mouseleave', function() {
                summary.style.display = 'block';
                content.style.display = 'none';
                this.style.height = '220px';
                this.style.zIndex = '1';
            });
            
            elements.storiesGrid.appendChild(storyCard);
        });
    }

    function searchByLesson() {
        const searchTerm = elements.searchInput.value.trim().toLowerCase();
        
        if (searchTerm === '') {
            displayStories(currentStories);
            return;
        }
        
        const filteredStories = currentStories.filter(story => 
            story.lessons.some(lesson => 
                lesson.toLowerCase().includes(searchTerm)) || 
            story.title.toLowerCase().includes(searchTerm) ||
            story.summary.toLowerCase().includes(searchTerm)
        );
        
        displayStories(filteredStories);
    }
